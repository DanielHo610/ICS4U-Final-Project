public class Rectangle {
    private double x;
    private double y;
    private double width;
    private double height;
    Rectangle(double x, double y, double width, double height){
    }
    // Getters and Setters
    public double getX(){
        return this.x;
    }
    public double getY(){
        return this.y;
    }
    public double getW(){
        return this.width;
    }    
    public double getH(){
        return this.height;
    }   
}
